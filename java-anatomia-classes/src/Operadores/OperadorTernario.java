package Operadores;

public class OperadorTernario {
    
    public static void main(String[] args) {
        ////////////Operador Ternário /////
     //Operador ternário ?:

     int a , b ;
     a = 6;
     b = 6;
     
     String resultado = a == b ? "verdadeiro": "falso";
     int resultado2 = a == b ? 1 : 0 ;

     System.out.println(resultado);
     System.out.println(resultado2);

    } 

}
